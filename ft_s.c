#include "ft_printf.h"

int	ft_found_s(char *s, int i)
{
	if (!s)
		i = ft_found_s("(null)", i);
	else
	{
		while (*s)
		{
			write(1, &(*s), 1);
			i++;
			s++;
		}
	}
	return (i);
}
